pub fn random_verb() -> Result<String> {
    let mut rng = thread_rng();
    VERBS
        .choose(&mut rng)
        .map(|v| v.to_string())
        .with_context(|| "While choosing a random verb")
}

pub fn random_noun() -> Result<String> {
    let mut rng = thread_rng();
    NOUNS
        .choose(&mut rng)
        .map(|v| v.to_string())
        .with_context(|| "While choosing a random noun")
}
